/**
 * Represents type of target on the game map.
 */
public enum Target {
    /** Food target. */
    FOOD,
    
    /** Friendly hill target. */
    MY_HILL,
    
    /** Friendly ant target. */
    MY_ANT,
    
    /** Enemy hill target. */
    ENEMY_HILL,
    
    /** Enemy ant target. */
    ENEMY_ANT,
    
    /** Land target. */
    LAND;
}
